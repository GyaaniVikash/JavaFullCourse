package learnJava4_OOPS_2;

public class Inheritance_1_Basic {

	public static void main(String args[])
	{
		System.out.println(args.length);//run configuration >>arguments enter string and run
		
		for(String value:args)
		{
			System.out.print(value+" "); //to print string enter in arguments (right click)
		}
		/*Inheritance -- Acquiring all the properties(variables) & behavior(methods) from one class to another class
		 * Parent --> child
		 * Advantage -- Reusability and avoid duplication
		 * Types - 
		 * Single - one parent one child
		 * Multi level - parent > child > child of child
		 * Hierarchy -  one parent multiple child
		 * Multiple -  Multiple parent one child
		 */

	}

}
